class _Handler(BaseRequestHandler):

    def handle(self):
        # A message from an MCP device is expected to reply using the same
        # port number as the server. Thus, ignore anything from anyone else.
        if self.client_address[1] != PORT:
            return
        self.server.handle(
            self.client_address[0]
        )


class SennheiserMCPDiscovery(Thread):

    def __init__(self):
        super().__init__(daemon=True)
        self._server = _Server(("0.0.0.0", PORT), _Handler)
        self.discovered = self._server.discovered

    def discover(self):
        self._server.discover()

    def run(self):
        with self._server as server:
            logger.info(
                "Listening for discovery-response messages from " \
                "Sennheiser radio-mic receivers via UDP on port %s",
                PORT
            )
            server.serve_forever()

    def stop(self):
        if self.is_alive():
            self._server.shutdown()
            self.join()


class _Server(UDPServer):

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self.discovered = Signal()

    def handle(self, addr):
        self.discovered.emit(addr)

    def discover(self):
        for iface in ni.interfaces():
            try:
                for addr in ni.ifaddresses(iface)[ni.AF_INET]:
                    with socket.socket(socket.AF_INET, socket.SOCK_DGRAM) as sock:
                        sock.bind((addr['addr'], PORT + 1))
                        sock.sendto(DISCOVERY_MSG, (MCAST_IP, PORT))

            except KeyError:
                continue
